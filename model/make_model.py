def make_model(cfg, num_class, num_attribute, camera_num, view_num):
    if cfg.MODEL.NAME == 'transformer':
        if cfg.MODEL.ATTR:  # with GRL module
            if cfg.MODEL.JPM: # ViT + JPM + GRL, ViT + JPM + SIE + GRL
                model = build_transformer_vit_local_grl(num_class, num_attribute, camera_num, view_num, cfg, __factory_T_type, rearrange=cfg.MODEL.RE_ARRANGE)
                print('===========================================================================')
                print('===========building [ViT + JPM + GRL] OR [ViT + JPM + SIE + GRL]===========')
                print('===========================================================================')
            else: # ViT + SIE + GRL, ViT + GRL
                model = build_transformer_vit_grl(num_class, num_attribute, camera_num, view_num, cfg, __factory_T_type)
                print('===========================================================================')
                print('=================building [ViT + SIE + GRL] OR [ViT + GRL]=================')
                print('===========================================================================')
        else:   # without GRL module
            if cfg.MODEL.JPM: # ViT + JPM, ViT + JPM + SIE
                model = build_transformer_local(num_class, camera_num, view_num, cfg, __factory_T_type, rearrange=cfg.MODEL.RE_ARRANGE)
                print('===========================================================================')
                print('=================building [ViT + JPM] OR [ViT + JPM + SIE]=================')
                print('===========================================================================')
            else:   # ViT, ViT + SIE
                model = build_transformer(num_class, camera_num, view_num, cfg, __factory_T_type)
                print('===========================================================================')
                print('=======================building [ViT] OR [ViT + SIE]=======================')
                print('===========================================================================')
    elif cfg.MODEL.NAME == 'swin':
        if cfg.MODEL.ATTR:  # with GRL module
            if cfg.MODEL.SEGKEY:  # SWIN + SEGKEY + GRL, SWIN + SIE + SEGKEY + GRL
                model = build_transformer_swin_segkey_grl(num_class, num_attribute, camera_num, view_num, cfg, __factory_T_type)
                print('===========================================================================')
                print('=======building [SWIN + SEGKEY + GRL] OR [SWIN + SIE + SEGKEY + GRL]=======')
                print('===========================================================================')
            else:   # SWIN + GRL, SWIN + SIE + GRL
                model = build_transformer_swin_grl(num_class, num_attribute, camera_num, view_num, cfg, __factory_T_type)
                print('===========================================================================')
                print('================building [SWIN + GRL] OR [SWIN + SIE + GRL]================')
                print('===========================================================================')
        else:   # without GRL module
            if cfg.MODEL.SEGKEY:  # SWIN + SEGKEY, SWIN + SIE + SEGKEY
                model = build_transformer_swin_segkey(num_class, camera_num, view_num, cfg, __factory_T_type)
                print('===========================================================================')
                print('=============building [SWIN + SEGKEY] OR [SWIN + SIE + SEGKEY]=============')
                print('===========================================================================')
            else:   # SWIN, SWIN + SIE
                model = build_transformer_swin(num_class, camera_num, view_num, cfg, __factory_T_type)
                print('===========================================================================')
                print('======================building [SWIN] OR [SWIN + SIE]======================')
                print('===========================================================================')
    else:
        model = Backbone(num_class, cfg)
        print('===========building ResNet===========')
    return model
